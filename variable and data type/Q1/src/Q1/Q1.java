package Q1;

public class Q1 {
	public static void main(String[] args) {
		float a, b;
		float sum, sub, mul;// 합, 차, 곱
		
		a=1.54f;
		b=25.1652f;
		
		sum=a+b;
		sub=a-b;
		mul=a*b;
		
		System.out.println("a+b: "+sum);
		System.out.println("a-b: "+sub);
		System.out.println("a*b: "+mul);
		
	}
}
